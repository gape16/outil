	<h3 class="title">Votre favicon est-il cohérent ?</h3>
	<p>Un <b>favicon</b> est une petite image que l’on retrouve le plus souvent à côté de l’adresse Internet de votre site ou bien en haut à gauche dans les onglets concernant les pages de votre site. Cette image doit avoir une taille bien précise.</p>
	<h3 class="title" >Le favicon doit respecter les conditions suivantes :</h3>
	<ul>
		<li>Fichier png de <b>32×32 pixles</b></li>
		<li>
			Ne pas dépasser <b>256 Couleurs</b></li>
			<li>Dans le cas d’un logo qui devient <b>illisible</b>, on peut extraire le symbole (sans texte) et l’utiliser, on peut également utiliser la <b>première lettre</b> ou des <b>acronymes</b>.</li>
		</ul>


