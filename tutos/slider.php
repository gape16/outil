	<h3 class="title">Le slider</h3>
	<p>Un <b>slider</b> contient généralement, un <b>CTA</b>, une <b>catchphrase</b> qui définit l’activité du client ainsi qu’une <b>photo représentative</b> de son domaine d’intervention. Le slider est un excellent moyen pour transmettre les informations importantes à vos utilisateurs.

		L’utilisation d’un slider <b>n’est pas obligatoire</b> dans la conception d’un site web.

	Voici quelques éléments à prendre en compte lors de la configuration ou de la modification du slider de votre page d’accueil.</p>
	<h3 class="title" >Le slider doit respecter les conditions suivantes :</h3>
	<ul>
		<li>Ne pas <b>ralentir</b> de trop le chargement du site</li>
		<li>Chaque slide/scène doit <b>raconter</b> une histoire ou transmettre un message</li>
		<li>Utiliser l’appel à des actions pour que les utilisateurs <b>savent</b> qu’il ya plus de contenu à venir et créer de l'<b>interactivité</b></li>
		<li>Dans le cas où on utilise une vidéo dans le slider, <b>éviter l’autoplay</b> et penser à laisser le <b>contrôle du volume</b> et de la <b>timeline</b> à l’utilisateur final.</li>
	</ul>

	<h3 class="title">Les alternatives</h3>
	<p>Si vous n’avez pas d’élément pour étoffer un slider, pas assez d’informations, ou <b>pas assez de visuels engageants</b>, n’hésitez pas à changer de technique.</p>

