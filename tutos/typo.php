	<h3 class="title">Les typos</h3>
	<img src="img/tuto/typo1.png" class="img-typo">
	<h4 class="title">1. Choisissez une typo en harmonie avec votre design</h4>
	<p>Lors de l'élaboration de votre site, vous devez avoir une idée claire de ce que vous voulez transmettre (divertissement, professionnalisme, créativité, etc.), quels secteurs viser (hôtellerie, éducation, tourisme, etc.) et évoquer les idées avec les formes, les couleurs et les textes qui composeront celui-ci.

		Le texte doit contribuer au visuel : il ne doit pas s'agir d'un élément isolé mais au contraire d'un élément intégré. C'est pourquoi il est important de choisir une police qui soit liée à l'idée principale.
	</p>

	<img src="img/tuto/typo2.jpg" class="img-typo">
	<h4 class="title">2. Optez pour trois polices maximum</h4>
	<p>L'utilisation de plusieurs polices peut donner une impression de désordre et de chaos et rend plus difficile la distinction entre le corps du texte principal et le texte secondaire.
	Lors du choix de la police, nous vous recommandons de :	</p>
	<ul>
		<li>sélectionner des polices de la même famille (par ex.: Times, Helvetica, etc.)</li>
		<li>choisir une police pour le titre et une autre police pour le corps du texte</li>
	</ul>


	<img src="img/tuto/typo3.png" class="img-typo">
	<h4 class="title">3. N'oubliez pas les polices traditionnelles</h4>
	<p>Si choisir d'innover grâce à une police a beaucoup d'avantages, il faut toutefois savoir comment l'utiliser pour arriver à un bon résultat. Si vous n'êtes pas entièrement sûr de la police à utiliser, il est préférable de garder une typographie et une police d'écriture "traditionnelle". Succès garanti !</p>

	<img src="img/tuto/typo4.jpg" class="img-typo">
	<h4 class="title">4. Jouez avec la taille de la police (sans exagérer)</h4>
	<p>Afin de différencier un titre d'un autre, on peut être tenté de jouer avec la taille et le style, par exemple : Arial Narrow, Rounded, Black...

	Utilisez la typographie Bold (communément appelée "gras") pour accentuer une idée. Mais rappelez-vous : n'abusez jamais de ces ressources au risque de perdre le sens voulu.</p>

	<img src="img/tuto/typo5.png" class="img-typo">
	<h4 class="title">5. Utilisez une police lisible</h4>
	<p>Le texte doit être conçu pour être lu. De ce fait, il est de mise de rendre la tâche des visiteurs aisée. Voici trois points sur lesquels il est conseillé de vous concentrer :</p>
	<ul>
		<li>Taille : choisissez une taille appropriée au corps du texte</li>
		<li>Couleur : le texte doit contraster avec la couleur de fond</li>
		<li>Espace : aérez vos textes, pensez à utilisez des retours à la ligne et créer des paragraphes.</li>
	</ul>

	<a href="https://www.typetester.org/"><img src="img/tuto/typo6.png" class="img-typo"></a>
	<h4 class="title">> Pour aller plus loin : TypeTester</h4>
	<p>TypeTester est un outil vous permettant de comparer plusieurs polices entre elles. Ce service est gratuit et disponible <a href="https://www.typetester.org/">ici</a>.

		Cet outil est assez intéressant puisqu'il permet de comparer en même temps trois polices, selon des critères tels que la taille, l'alignement ou encore la couleur de ces dernières.

	Le cadre principal vous permet de taper un texte souhaité et ensuite de sélectionner les polices de votre choix, afin que de voir si cela pourrait avoir un bon rendu sur votre site Jimdo !</p>

	

