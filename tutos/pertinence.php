	<h3 class="title">Pertinence design</h3>
	<div class="row">
		<div class="col-xl-6 col-lg-12 col-md-12 col-sm-12 col-xs-12">
			<div class="main-header-content">
				<h4 class="title">Personnalisation</h4>
				<p>Le site doit être <b>personnalisé</b>. Le but étant de trouver un compromis entre les demandes client et le style du graphiste. Le client doit avoir sa propre <b>identité graphique</b>, basé sur son logo, des formes, des couleurs,… Chaque site doit être « unique », bien entendu il peut être similaire à des sites déjà existants mais il doit avoir quelques détails qui permet de le <b>distinguer des autres</b>.</p>
			</div>
		</div>
		<div class="col-xl-6 col-lg-12 col-md-12 col-sm-12 col-xs-12">
			<div class="main-header-content">
				<h4 class="title">Cohérence</h4>
				<p>Le site doit posséder une <b>charte graphique</b> qui se suit du <b>début à la fin</b>. On retrouver les mêmes formes, les mêmes styles sur les images et les titres, les même couleurs,… Sur les pages d’activités, on doit <b>retrouver</b> le style graphique de l’accueil. Bien sûr, il peut y avoir quelques <b>variantes</b> pour casser un peu la linéarité du site cependant on ne doit pas avoir l’impression qu’il sort de nulle part mais qu’il est pensé pour se fondre dans le site.</p>
			</div>
		</div>
		<div class="col-xl-6 col-lg-12 col-md-12 col-sm-12 col-xs-12">
			<div class="main-header-content">
				<h4 class="title">Identification</h4>
				<p>Le site doit représenter l’<b>activité du client</b>. A l’aide de visuels, d’éléments graphiques (pictos, fonds, code visuels,…), on doit être capable d’<b>identifier</b> ce que fait le client et ce qu’il peut nous proposer.</p>
			</div>
		</div>
		<div class="col-xl-6 col-lg-12 col-md-12 col-sm-12 col-xs-12">
			<div class="main-header-content">
				<h4 class="title">Épuré</h4>
				<p>Un site ne doit <b>pas être trop chargé</b>. L’internaute doit pouvoir <b>trouver</b> son information <b>rapidement</b> et avoir la navigation la plus <b>simple possible</b>. Le design est là pour lui <b>mettre en avant</b> les avantages du client, ce qui le caractérise, les bonnes affaires,… et pour le guider sur le site.</p>
			</div>
		</div>
	</div>

	<p class="mt"><b>Bonne pratique</b> : Le mieux pour bien réussir un design est d’aborder de faire un <b>travail préparatoire</b>. Etudier ce que peut faire la concurrence, <b>faire de la veille</b> et ensuite faire un croquis sur une feuille avant de se lancer dans l’intégration car sinon le site à très peu de chances d’avoir une <b>ligne directrice</b>.</p>

