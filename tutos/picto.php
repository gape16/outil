	<h4 class="title">Cohérence graphique</h4>
	<p>Les pictos ont pleins de fonctions utiles : mettre en valeur les atouts de l’entreprise, aider à la navigation, illustrer un texte,… Contrairement à ce que l’on peut croire, les pictos ne sont pas obligés d’être rassemblés au même endroit.</p>
	<div class="row">
		<div class="col-xl-6 col-lg-12 col-md-12 col-sm-12 col-xs-12">
			<h3 class="title">Design</h3>
			<p>Le plus important pour le picto est d’être en accord avec la charte du site. Il est nécessaire d’y retrouver une couleur, une forme ou un fond qui peut être retrouvé autre part sur le site. Les pictos ne doivent pas être pixélisé et épuré. Un picto trop chargé perdra de sa lisibilité et n’aura pas l’impact escompté.

				<b>La chose à ne surtout pas faire</b> : Aller sur flaticon.com, télécharger le picto et le mettre directement sur le site. Un picto doit être penser et travailler.</p>
			</div>
			<div class="col-xl-6 col-lg-12 col-md-12 col-sm-12 col-xs-12">
				<h3 class="title">Identifiable</h3>
				<p>Le picto doit être identifiable au maximum, pour que sans lire on arrive à comprendre ce qu’il veut dire. C’est peut-être la chose la plus dure à faire sur un picto. S’il est en décalage par rapport à ce qu’il représente (ex : Service +, Devis gratuit,…), l’internaute peut ne pas comprendre et on peut donc perdre l’intérêt l’élément auquel est associé le picto.

				La chose à ne surtout pas faire : Un picto n’est pas un CTA, il faut donc éviter une incitation au clic (effet hover, forme d’un cta,…).</p>
			</div>
