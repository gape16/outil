	<h3 class="title">Les pictos/labels</h3>
	<p>Lors de l’affichage initial d’un site Web, la plupart des utilisateurs balayeront d’abord la page pour le contenu visuellement intéressant, et seulement après que quelque chose saisit leur attention qu’ils commencent réellement à lire. Les icônes sont un moyen simple et efficace pour attirer les utilisateurs dans le contenu de votre site Web.

	Les icônes servent le même but psychologique que les pauses de paragraphe: elles divisent visuellement le contenu, le rendant moins intimidant. Une page bien formatée, avec du texte divisé en paragraphes facilement accessibles et accentués par des icônes, est facile à lire et suffisamment visuelle pour soutenir l’attention de l’utilisateur.	</p>

