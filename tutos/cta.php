	<h3 class="title">Les CTA</h3>
	<p>Un CTA (Call to Action = appel à l’action) est un bouton ou un lien que vous placez sur votre site Web pour inciter les clients potentiels à devenir des prospects en remplissant un formulaire sur une page de destination. Le but d’un CTA est de guider les utilisateurs vers la page de conversion et prendre l’action que vous souhaitez qu’ils prennent. Les Appels à l’action peuvent varier en style et la taille en fonction de votre objectif et du style du site.</p>

	<h4 class="title">Les meilleures pratiques</h4>
	<p>Suivez ces directives pour créer des CTA qui respectent les meilleures pratiques établies.</p>


	<h4 class="title">Les CTA devraient être :</h4>
	<ul>
		<li>Visuellement frappant avec une copie qui vous oblige à cliquer sur l’offre</li>
		<li>Bref: Un couple de mots est préférable, pas plus de cinq est idéal</li>
		<li>Action-oriented: Commencez par un verbe comme « Download » ou « Register »</li>
		<li>Situé dans un endroit facile à trouver qui suit organiquement du flux de la page Web</li>
		<li>Dans une couleur contrastante de la couleur de la page Web, tout en s’inscrivant dans la conception globale</li>
		<li>Assez grand pour voir de loin, mais pas aussi grand que pour détourner l’attention du contenu principal sur la page</li>
		<li>Facile à comprendre et clair: Assurez-vous d’indiquer exactement ce que le visiteur obtiendra si elles cliquent sur le CTA et aller à la page de destination</li>
	</ul>

