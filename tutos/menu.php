	<h3 class="title">Le menu</h3>
	<p>Le <b>menu</b> est incontestablement le moyen de navigation le <b>plus important</b> pour pour presque tous les sites Web et applications. <br>

	Afin qu’il réussisse sa mission, tout menu doit être : </p>
	<div class="row">
		<div class="col-xl-6 col-lg-12 col-md-12 col-sm-12 col-xs-12">
			<div class="main-header-content">
				<h4 class="title">Visible</h4>
				<p>Les <b>menus hamburger</b> doivent être évités sur desktop. Le menu doit être placé en haut ou sur un coté avec un <b>contraste</b> suffisant entre les liens du menu et le fond. La <b>taille</b> du texte est généralement plus importante que pour le reste du site.</p>
			</div>
		</div>
		<div class="col-xl-6 col-lg-12 col-md-12 col-sm-12 col-xs-12">
			<div class="main-header-content">
				<h4 class="title">Simple</h4>
				<p>Éviter de rendre la navigation difficile à comprendre. Par exemple,  une navigation <b>pleine de menus</b> et de <b>sous-menus encombrés</b> d’une manière désorganisée, rend <b>difficile</b> pour les utilisateurs de naviguer à travers le site Web.</p>
			</div>
		</div>
		<div class="col-xl-6 col-lg-12 col-md-12 col-sm-12 col-xs-12 margin">
			<div class="main-header-content">
				<h4 class="title">Cohérent</h4>
				<p>Il est toujours bon de garder le thème et la structure du menu <b>cohérente</b>. L’utilisateur s’attend à ce que toutes les pages soient <b>similaires en termes de structure</b>. Une navigation différente à chaque page ne fera que frustrer l’utilisateur.</p>
			</div>
		</div>
		<div class="col-xl-6 col-lg-12 col-md-12 col-sm-12 col-xs-12 margin">
			<div class="main-header-content">
				<h4 class="title">Structuré</h4>
				<p>Le menu de doit avoir une <b>structure hiérarchique claire</b>. Cela donne à l’utilisateur une idée précise de ce que vous pouvez offrir sans avoir à passer par plusieurs pages pour <b>trouver</b> ce qu’ils recherchent.</p>
			</div>
		</div>
	</div>

	<h2 class="title" >Menu horizontal</h3>
		<h4 class="title" >Un menu horizontal doit repecter les conditions suivantes :</h4>
		<ul>
			<li>Rester sur une ligne sur toute résolution <b>supérieure à 1240px de largeur</b></li>
			<li>Ne pas dépasser <b>deux mots</b> par élément.</li>
		</ul>

		<h2 class="title" >Menu creative</h3>
			<p>Pour ce genre de menu, la chose à ne surtout pas oublier est l’<b>option «scrollable»</b> auquel il faut penser si jamais il y a trop d’onglet sur le menu au point de ne pas pouvoir tous les mettre sur la hauteur (cf mauvais exemple de menu)</p>


			<img class="help-image" src="img/tuto/options-scrollable.png" alt="screen">

			<h2 class="title" >Menu overlay</h3>
				<p>Pour bien paramétrer le menu, il suffit juste de vérifier si les liens sont bien <b>visibles</b> par rapport au fond et mettre une couleur sur le burger menu. Si jamais votre site comporte des grands background images, il faut penser à mettre un <b>fond moins transparent</b> sur le menu.</p>

