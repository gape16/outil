	<h3 class="title">7 principes fondamentaux de design</h3>
	<p>Le design de votre site est plus important que vous ne le pensez pour améliorer vos conversions. Peu importe les tactiques que vous avez déjà mises en oeuvre pour booster votre taux de conversion, si votre site ne ressemble à rien : vous gaspillez vos ressources.</p>
	<ul>
		<li>Le design ne se réduit pas au travail d’un graphiste.</li>
		<li>Le design, c’est aussi du marketing.</li>
		<li>Le design, c’est votre produit et son fonctionnement.</li>
	</ul>
	<p>Vous trouverez ici 7 principes fondamentaux d’ergonomie et de design qui vous permettront d’améliorer votre site internet. Ignorez-les à vos risques et périls.</p>

	<h4 class="title">1. La hiérarchie visuelle</h4>
	<p>La hiérarchie visuelle est l’un des principes fondamentaux du web design. Il s’agit de l’ordre dans lequel l’œil humain perçoit les choses.</p>
	<p><i>Exercice. Classez ces cercles par ordre d’importance :</i></p>
	<img src="img/tuto/hierarchie_visuelle.jpg" class="img-typo mb">
	<p>Alors même que vous ne savez RIEN de ces cercles, <b>vous avez pu les classer sans effort</b>. Leur hiérarchie visuelle s’est imposée à vous.

		Ce principe est également applicable à votre site internet. Certains éléments sont plus importants que d’autres (formulaires, boutons, proposition de valeur, etc.) et ils doivent facilement <b>attirer l’attention</b> de vos prospects.

		Si votre menu de navigation affiche 10 liens, demandez-vous s’ils ont tous la même importance. Où voulez-vous que l’utilisateur clique ? Faites en sorte que les liens <b>les plus importants</b> soient <b>plus visibles</b> que les autres.

		<b>La hiérarchie visuelle n’est pas uniquement une question de taille</b>. Prenons l’exemple d’Amazon : le bouton “Ajouter au Panier” attire immédiatement l’attention au détriment du bouton secondaire grâce à <b>sa couleur</b>.
	</p>
	<img src="img/tuto/addtocart.png" class="img-typo mb">

	<p><b>Commencez toujours par l’objectif de votre entreprise</b>. Quel est votre objectif principal quand un client arrive sur votre site web ? Cet <b>objectif doit déterminer l’ordre dans lequel les éléments de votre site web sont hiérarchisés</b>. Si vous ne vous êtes pas fixé de but précis, vous ne pourrez pas établir de hiérarchie.

	Voici un exemple, c’est une capture d’écran du site Becquet.fr, une boutique de linge de maison.</p>
	<img src="img/tuto/becquet.png" class="img-typo mb">
	<ul>
		<li>L’oeil se focalise d’abord sur le <b>visuel coloré et proéminent de linge</b> de lit (Mmh, il est peut-être temps de renouveler mes draps !),</li>
		<li>suivi de la <b>proposition de valeur</b> (« Réveillez votre intérieur » et surtout -40% de réduction sur le linge de lit)</li>
		<li>et le <b>bouton principal</b> (accéder à toute la sélection)</li>
		<li>Les quatrième et cinquième choses les plus visibles sont les <b>liens d’accès aux catégories en promotion</b> et les <b>éléments de réassurance</b> de la boutique</li>
		<li>La navigation est le dernier élément. C’est une bonne hiérarchie visuelle.</li>
	</ul>

	<h4 class="title">2. Les grilles de composition</h4>
	<blockquote>"Il y a quelque chose de différent entre ce site et le mien… Il a l'air de faire plus pro et je ne sais pas pourquoi !"</blockquote>
	<p>N'avez-vous jamais eu ce genre de remarque ? Pourtant vous utilisez Photoshop (ou autres programmes équivalents) depuis un certain temps, vous connaissez les règles typographiques et celles du web, la palette de couleur a été choisie avec soin... Bref, rien n'y fait : votre site semble déstructuré / désharmonisé.</p>
	<img src="img/tuto/intro2.jpg" class="img-typo mb">
	<p>Pas d'inquiétude, vous n'avez sans doute pas utilisé de grille dans votre design !</p>
	<h5 class="title">Une grille, c'est quoi ?</h5>
	<p>Utilisée en imprimerie, il est tout à fait possible de transposer le système de grille dans le domaine du web. Mais attention le web n'a pas, et ne doit pas, forcément avoir les mêmes caractéristiques et contraintes que le support papier. Pour résumé : une page web n'a pas de hauteur ou de largeur prévisible contrairement à l'impression.</p>
	<p class="faketitle">Grilles : les bases</p>
	<p>Il existe beaucoup de grilles différentes avec autant de cas concrets d'utilisations. Nous allons parler ici uniquement des grilles "standards" dédiées au média web. Bien entendu, vous pouvez créer votre propre grille : mais ce n'est pas si simple qu'il n'y paraît - évitons de réinventer la roue.</p>
	<p>La majorité des grilles disposent de marges latérales, afin que l'œil puisse bénéficier d'un confort de lecture en ne "tapant" pas contre les bords de la fenêtre du navigateur.</p>
	<p class="faketitle">Anatomie d'une grille</p>
	<p>Une grille est constituée de repères (axes) verticaux et/ou horizontaux, séparés par des gouttières permettant de structurer le contenu. Il ne faut pas oublier les marges externes (pour les éventuels bords de la fenêtre) et internes (de chaque axe).

		Elle sert d'armature pour organiser la page et son contenu. C'est une aide autant pour le designer que pour le visiteur. Cela permet d'atteindre un équilibre harmonieux entre l'espacement et l'organisation des différents blocs.

	On obtient alors une page plus lisible et plus homogène. Sans grille, un site avec beaucoup de contenu risque d'être moins agréable à visiter.</p>
	<img src="img/tuto/anatomie-grille.jpg" class="img-typo mb">
	<p>Il est facile d'imaginer les différentes variantes possibles de grilles en jouant sur : le nombre de colonne, leur largeur, la largeur des gouttières, les marges...</p>
	<p class="faketitle">Les grilles verticales</p>
	<p>On peut choisir le nombre de colonnes à insérer dans une largeur fixe. Par conséquence : moins il y a de colonnes plus elles sont larges.</p>
	<p>Exemple :</p>
	<img src="img/tuto/12-col.jpg" class="img-typo mb">
	<img src="img/tuto/16-col.jpg" class="img-typo mb">
	<img src="img/tuto/24-cols.jpg" class="img-typo mb">
	<p>Ces colonnes permettent de donner un rythme de division dans le sens horizontal (de gauche à droite). La plupart du temps cela suffit pour créer un design.</p>
	<p class="faketitle">Les grilles verticales et horizontales</p>
	<p>Ajouter des divisions horizontales permet de donner des repères dans le sens vertical (de haut en bas).</p>
	<p>Exemple :</p>
	<img src="img/tuto/hori-verti2.jpg" class="img-typo mb">
	<img src="img/tuto/carre2.jpg" class="img-typo mb">
	<p>C'est extrêmement pratique pour aligner plusieurs blocs qui sont côtes à côtes.</p>
	<h4 class="title">Comment choisir sa grille ?</h4>
	<p>Il n'y a pas vraiment de règle type : tout dépend du projet. Il est donc très important de bien préparer son projet en aval pour, entre autre, bien cerner le contenu du site. 

	Votre grille devra donc être compatible avec les longueurs de texte, le type de site (institutionnel, portfolio, blog/magazine...), etc. On peut penser que plus il y a de contenu, plus nous aurons besoin de colonnes dans la grille. Mais ce n'est pas une règle absolue, il s'agit toujours de faire un compromis entre quantité d'information présentée à l'écran et lisibilité.</p>
	<p class="faketitle">Où trouver des grilles</p>
	<p>De nombreux sites proposent de télécharger des modèles de grilles. Cependant, certains sites ne proposent que des Frameworks CSS qui ne sont pas toujours utilisables directement dans un logiciel de design.</p>
	<p>Liste où trouver des grilles : <a href="http://960.gs/">960 grid</a>, <a href="http://modulargrid.org/#app">Modular Grid Pattern</a>, <a href="https://guideguide.me/">GuideGuide (add-on PS & payant)</a></p>
	




	<h4 class="title">3. Le nombre d’Or</h4>
	<p>Le nombre d’or (1,618) est un ratio « magique » qui permet de réaliser des interfaces aux proportions esthétiquement parfaites – du moins, un certain sens de l’équilibre et de l’harmonie.

	Voici à quoi ressemble le nombre d’or :</p>
	<img src="img/tuto/spirale_fibonacci.png" class="img-typo mb">
	<p>Beaucoup d’artistes et d’architectes ont proportionné leur travail en s’inspirant du nombre d’or. Le Parthénon, à Athènes, est sans doute l’exemple le plus célèbre :</p>
	<img src="img/tuto/nombre_dor_design.png" class="img-typo mb">

	<p class="center"><b>Est-ce qu’on l’utilise de nos jours ? Pour réaliser des sites internet par exemple ?</b></p>
	<p>Twitter le fait...</p>
	<img src="img/tuto/proportion_design_twitter_nombre_or.jpg" class="img-typo mb">
	<p>voici ce que Doug Bowman, le directeur artistique de Twitter, a révélé après la sortie de la nouvelle version du site : “Pour ceux qui s’interrogent sur le nouveau design de Twitter, sachez que nous n’avons pas choisi ces proportions par hasard. ”</p>
	<blockquote><span class="big">"</span> Donc, si la largeur de votre page est de 960 pixels, divisez-la par 1,618 (=593 pixels).

		La zone de contenu doit mesurer 593px1
		et la barre latérale 367px.
		Si la hauteur de votre site est de 760px,

		vous pouvez le scinder en deux parties de 470px pour la zone de contenu
	et 290px pour le pied de page (760/1.618 = ~470)</blockquote>


	<h4 class="title">4. La loi de Hick</h4>
	<blockquote><span class="big">"</span> La loi de Hick énonce que chaque choix supplémentaire augmente le temps nécessaire pour prendre une décision.</blockquote>
	<p>Pensez à toutes ces fois où vous vous êtes retrouvé au restaurant à devoir choisir parmi un menu sans fin… C’est tout le temps un casse-tête. En revanche, si le menu vous permettait de choisir uniquement entre deux plats, ce serait beaucoup plus rapide. Ce principe s’apparente au « Paradoxe du Choix » : <b>plus vous donnez de choix aux gens, moins ils choisissent</b>.</p>

	<p>Vous pouvez améliorer l’expérience de vos clients en réduisant le nombre de choix disponibles. Ces décisions doivent être prises dès le début du processus de création de votre site.<b> Ne cédez pas à la tentation de rajouter des options en cours de réalisation</b> du design et vous devriez vous en sortir.</p>

	<p>Lorsqu’on vend une <b>grande quantité de produits, on a besoin de filtres</b> ! Si vous gérez une boutique en ligne avec un important catalogue, affichez progressivement les choix qui s’offrent à vos clients en utilisant une navigation par filtres.</p>

	<p>Par exemple, Urban Outfitters a une offre extrêmement large de vêtements et de chaussures. L’utilisation de filtres leur permet de ne pas afficher l’intégralité des catégories et des sous-catégories de leur catalogue sur la page d’accueil de leur site.</p>

	<img src="img/tuto/navigation_facette_filtres_choix.png" class="img-typo mb">

	<h4 class="title">5. La loi de Fitts</h4>
	<p>La loi de Fitts stipule que le temps requis pour se déplacer vers une zone cible (par exemple : cliquer sur un bouton) est une fonction de la distance de la cible sur la taille de la cible.</p>
	<blockquote><span class="big">"</span> Autrement dit, plus un objet est gros et proche, plus il est facile à atteindre.</blockquote>

	<p class="center">Deezer facilite la sélection du bouton « Lecture » par rapport aux autres :</p>
	<img src="img/tuto/loi_fitts_deezer.png"  class="img-typo mb">

	<p><b>Ca ne veut pas forcément dire qu’il faut augmenter la taille de vos boutons à tout prix</b>. Un bouton qui prend la moitié de l’écran n’est pas une bonne idée : pas besoin de citer une étude mathématique pour le savoir. Cela étant, la loi de Fitts est un logarithme binaire.</p>

	<p>Du coup, un bouton minuscule deviendra beaucoup plus facile à cliquer lorsque vous augmentez sa taille de 20 % tandis qu’augmenter dans les mêmes proportions la taille d’un bouton déjà très gros représente un avantage négligeable.</p>

	<p>Concrètement, <b>la taille d’un bouton doit être proportionnelle à sa fréquence d’utilisation</b>. Vous devriez jeter un oeil à vos statistiques pour savoir quels boutons sont les plus utilisés afin d’augmenter leur taille (et faciliter leur sélection).</p>

	<h4 class="title">6. La règle des Tiers</h4>
	<p>Vous utilisez probablement déjà des images sur votre site. <b>Une image communiquera toujours vos idées plus rapidement que n’importe quel texte</b>.</p>

	<ul>
		<li>En gros, vos images sont divisibles en neuf parties égales.</li>
		<li>Les 4 points formés par les intersections du quadrillage servent ainsi à aligner les éléments les plus importants de votre image.</li>
	</ul>

	<p><b>Ce type de composition attire plus facilement l’oeil humain sur l’objet</b> que vous voulez mettre en avant que lorsque vous centrez simplement la photo. Cette méthode vous permettra de tirer le maximum des visuels de votre site !</p>

	<img src="img/tuto/règle_tiers_design_photo_ux.gif"  class="img-typo mb">


	<h4 class="title">7. La théorie de Gestalt et les principes de la forme</h4>
	<p>Cette théorie psychologique postule que <b>l’œil humain perçoit les objets dans leur intégralité avant de les appréhnder comme éléments individuels</b>.

	Si je prends cette image par exemple:</p>

	<img src="img/tuto/gestalt_design_ux.gif"  class="img-typo mb">

	<p>Vous avez remarqué de quelle manière <b>vous avez reconnu un chien</b> sans avoir eu à vous concentrer sur chacun des points noirs qui le constituent ?</p>
	<blockquote><span class="big">"</span> Nous voyons toujours une image dans son ensemble avant de nous concentrer sur les éléments individuels qui la constituent.</blockquote>

	<p><b>Les gens perçoivent toujours l’intégralité de votre site en premier</b>. C’est seulement dans un second temps qu’ils vont percevoir la bannière d’en-tête, le menu, le pied de page et ainsi de suite.</p>

	<p>Il existe <b>8 lois de Gestalt</b> pour nous aider à anticiper la manière dont les gens vont percevoir notre site. Les voici :</p>

	<h5 class="title">1- La loi de la proximité</h5>
	<ul>
		<li>
			<p>Les gens <b>regroupent d’abord les objets les plus proches</b> dans l’espace. Si des éléments sont trop proches les uns des autres, ils seront perçus comme un seul et même objet. En matière de web design, assurez-vous que les éléments d’une page n’appartenant PAS au même groupe ne soient pas perçus comme un objet unique.</p>
			<img src="img/tuto/loi_proximité_gestalt.png"  class="img-typo mb">
		</li>
		<li>
			<p>De même, certains éléments de votre site doivent être regroupés (votre menu, le pied de page, etc.) pour montrer qu’ils font partie du même ensemble. Le site de petites annonces Vivastreet utilise cette méthode pour rendre visible l’appartenance d’une sous-catégorie à une catégorie principale.</p>
			<img src="img/tuto/gestalt_design_ux.jpg"  class="img-typo mb">
		</li>
	</ul>

	<h5 class="title">2- La loi de similitude</h5>
	<blockquote><span class="big">"</span> Lorsque la distance entre les différents objets ne nous permet pas de les distinguer, on a alors tendance à regrouper les plus similaires entre eux. Il peut s’agir d’une forme ou d’une couleur similaire ou d’autres caractéristiques.</blockquote>
	<p>Sur l’image ci-dessous, l’oeil humain va rassembler les points noirs dans un groupe et les points blancs dans un autre.</p>
	<img src="img/tuto/loi_similitude_design_ux.png"  class="img-typo mb">
	<p>Sur notre site, les avis de clients Clicboutic sont tous présentés de la même manière. Un visiteur <b>doit intuitivement comprendre que chaque bloc a le même contenu</b>.</p>
	<img src="img/tuto/loi_similitude_gelstat.png" class="img-typo mb">

	<h5 class="title">3- La loi de clotûre</h5>
	<p>Nous cherchons naturellement la perfection. C’est pour cette raison que <b>notre perception remplit les vides lorsqu’on observe des formes qui ne sont pas fermées</b>.</p>
	<p>Sur l’image ci-dessous, nous voyons un cercle et un carré bien que ces formes n’existent pas réellement.</p>
	<img src="img/tuto/loi_cloture_gestalt.png"  class="img-typo mb">
	<p>Sans cette illusion de la perception, nous verrions juste différentes lignes aux longueurs variables. Mais la loi de clôture nous permet de combiner les lignes en une forme pleine.</p>
	<p>Cela peut vous aider à réaliser votre prochain logo. Un bon exemple est celui de WWF, dessiné par Sir Peter Scott en 1961 :</p>
	<img src="img/tuto/logo_gestalt.png"  class="img-typo mb">

	<h5 class="title">4- La loi de symétrie</h5>
	<p><b>Notre esprit perçoit les objets comme étant symétriques</b> et se construisant autour d’un point central. C’est à la fois plus facile et plus rassurant de pouvoir séparer visuellement plusieurs objets en un nombre égal d’éléments symétriques.</p>
	<p>Lorsque nous voyons deux éléments symétriques qui n’ont pas de lien entre eux, nous allons naturellement les associer pour composer une forme cohérente.</p>
	<img src="img/tuto/loi_symétrie_design.png"  class="img-typo mb">
	<p class="center"><i>Si vous regardez l’image ci-dessus, vous voyez certainement trois paires de parenthèses symétriques. Au lieu de six parenthèses individuelles.</i></p>
	<p>Si votre site e-commerce est construit autour de trois colonnes, <b>assurez-vous que les colonnes de gauche et de droite ont la même largeur</b>. L’espacement entre les blocs doit également être identique au <b>risque de donner aux visiteurs un sentiment de déséquilibre</b>.</p>

	<h5 class="title">5- La loi de destin commun</h5>
	<p>Nous avons tendance à percevoir des objets comme des lignes ayant une trajectoire. Concrètement, <b>nous regroupons ensemble les objets ayant la même trajectoire, qui vont dans la même direction</b>.</p>
	<img src="img/tuto/destin_commun_gestalt.jpg"  class="img-typo mb">
	<p>Dans l’exemple du dessus, on regroupe mentalement les mains pointées en direction du logo car elles suivent la même direction.</p>
	<p>Vous pouvez <b>utiliser ce principe pour guider l’attention de vos visiteurs</b> vers un élément important d’une page de votre site (un formulaire d’inscription, votre proposition de valeur, un bouton etc.).</p>

	<h5 class="title">6- La loi de continuité</h5>
	<p><b>Lorsqu’on voit une ligne, on attribue à cette ligne une trajectoire</b>. Dans le cas d’une intersection entre plusieurs objets (par exemple des lignes), nous allons percevoir deux lignes comme deux entités ininterrompues.</p>
	<p>Même si elles se chevauchent, notre esprit continue à distinguer les lignes d’origine. Le studio graphique Fixel utilise ce principe pour associer les visages de l’équipe à leurs biographies respectives :</p>
	<img src="img/tuto/loi_continuité_design_gestalt.jpg"  class="img-typo mb">
	<p>Il existe d’autres <a href="https://fr.wikipedia.org/wiki/Psychologie_de_la_forme#Les_principales_lois_de_la_Gestalt">lois de Gestalt</a> comme la loi de la bonne figure ou de la familiarité mais je pense que celles qui sont présentées ici sont les plus utiles à connaitre.</p>

	<h5 class="title">7- Éspace négatif et design épuré</h5>
	<blockquote><span class="big">"</span> L’espace « négatif » est la partie d’une page laissée « vide ».</blockquote>
	<p>C’est l’espace qui sépare les visuels, les textes, les bordures,  l’espace entre les colonnes ou différentes images.</p>
	<p><b>Ne considérez pas cet espace comme simplement du « vide » et résistez à la tentation de le combler</b>. Il permet aux différents objets d’une page d’exister. L’espace négatif est la base d’une bonne hiérarchie de l’information, qu’il s’agisse de textes, de couleurs ou d’images.</p>
	<p><b>Une page sans espace négatif aura l’air lourde et surchargée</b>. Cela compliquera la lecture et la navigation de vos visiteurs et beaucoup d’entre eux ne feront même pas l’effort de lire ce que vous avez à dire.</p>
	<p>Si vous utilisez correctement les espaces négatifs, votre site web aura l’air « propre » et facile à parcourir. Un <b>design épuré est crucial pour communiquer clairement votre message</b> mais ça ne se résume pas à retirer du contenu. Pour obtenir un design propre et épuré, utilisez l’espace dont vous disposez, pas davantage.</p>
	<p>Le site de mobilier design Made.com fait un excellent usage de l’espace négatif :</p>
	<img src="img/tuto/espace_negatif_design_epure.png" class="img-typo mb">
	<p>Une juste utilisation des espaces négatifs <b>aidera vos visiteurs à se concentrer sur votre message principal</b> et vos images et facilitera la lecture de vos textes. Les espaces négatifs contribuent à souligner l’<b>élégance</b> de votre site, <b>améliorent la lisibilité</b> et <b>guident l’œil</b> de vos clients vers l’essentiel.</p>

